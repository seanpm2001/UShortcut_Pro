#!/bin/sh
# Start of script

# Shortcuts are available in Linux, but it isn't made as obvious as Windows.
# This tool is for people who want to easily make shortcuts on Linux, but are too afraid to use the command line, or want an easier way
# This script is confirmed to work on Debian, Ubuntu, Kubuntu, Xubuntu, Lubuntu, Edubuntu, Fedora, RHEL (Red Hat Enterprise Linux) Manjaro, and more.

ln -s ~/user /desktop/ # Username has to be entered manually, unless your username is `user` this script will create the shortcut
cd / # Changes directory to root
/b # I don't know how to add an enter, so this is a template
cd desktop # Changes directory to desktop
dir # It will show the desktop so that the user can see the generated shortcut

